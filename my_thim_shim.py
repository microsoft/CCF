class MyTHIMHandler(BaseHTTPRequestHandler):
    def do_GET(self):
        LOG.info("Doing GET, and lol I don't care what the path is.")
        LOG.info(f"But, for posterity, it was: {self.path=}")
        body = {"vcekCert": VCEK_CERT, "certificateChain": CERT_CHAIN}
        body = json.dumps(body).encode() + b"\n"
        self.send_response(HTTPStatus.OK)
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def log_message(self, fmt, *args):
        LOG.trace(f"MyTHIMHandler: {fmt % args}")


with socketserver.TCPServer(("127.2.3.4", 8989), MyTHIMHandler) as httpd:
    print(f"Serving...")
    try:
        httpd.serve_forever()
    except:
        print("\nInterrupted, shutting down")
    finally:
        httpd.server_close()
